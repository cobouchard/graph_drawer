package Save;

public class GraphicVertexDataContainer {

    public int id;

    public double centerx;
    public double centery;
    public String name;

    public GraphicVertexDataContainer(int id, double centerx, double centery, String name) {
        this.id = id;
        this.centerx = centerx;
        this.centery = centery;
        this.name = name;
    }
}
